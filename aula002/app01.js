// Operadores Relacionais
/*
> --> Maior
< --> menor
>= -> maior ou igual
<= -> menor ou igual
== -> igual
!= -> diferente 
*/

console.log(10>28)
console.log(10==10)
console.log(10!=10)
console.log('10'<10)
console.log(1<= 16)
console.log(4>=23)

